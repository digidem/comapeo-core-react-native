const {
  withGradleProperties,
  withPodfile,
  withXcodeProject,
} = require('@expo/config-plugins');

/**
 * Opts this Expo app into the benchmark JS bundle on every
 * `expo prebuild`. The bench app (`apps/benchmark/`) does not check in
 * `android/` or `ios/`, so this plugin is the single source of truth
 * for the native wiring; production consumers (`apps/example/`, third
 * parties) never apply it and never see any bench artefacts.
 *
 * Three idempotent mutations:
 *
 *   - `withGradleProperties` writes `comapeoBench=true` into the
 *     consuming app's `android/gradle.properties`. The module's
 *     `android/build.gradle` reads `rootProject.findProperty('comapeoBench')`
 *     and, when set, overlays `src/bench/assets/` (containing the bench
 *     `nodejs-project/index.mjs`) onto its main asset srcDirs. The
 *     property mechanism is intentionally chosen over a productFlavor
 *     to avoid AGP / Gradle 9 variant-attribute ambiguity for Expo apps
 *     that don't declare matching flavors of their own.
 *
 *   - `withPodfile` prepends `ENV['COMAPEO_BENCH'] = '1'` to the top of
 *     `ios/Podfile` so it's set BEFORE Expo autolinking evaluates
 *     `ComapeoCore.podspec`. The podspec reads that env var and swaps
 *     `nodejs-project-bench` in for `nodejs-project` in `s.resources`.
 *
 *   - `withXcodeProject` adds a Run Script build phase to the iOS app
 *     target that renames the embedded `nodejs-project-bench/` directory
 *     to `nodejs-project/` after Copy Bundle Resources. The native
 *     loader (`NodeJSService.swift`) reads from a fixed
 *     `<App>.app/nodejs-project/` path; this rename is what lets the
 *     bench bundle masquerade as the production bundle on disk
 *     without touching the loader.
 *
 * Each mutation is idempotent so re-runs of `expo prebuild` don't
 * accumulate duplicate insertions.
 */
function withComapeoBench(config) {
  config = withBenchGradleProperties(config);
  config = withBenchPodfile(config);
  config = withBenchIosRenameScript(config);
  return config;
}

function withBenchGradleProperties(config) {
  return withGradleProperties(config, (cfg) => {
    const props = cfg.modResults;
    const existing = props.find(
      (p) => p.type === 'property' && p.key === 'comapeoBench',
    );
    if (existing) {
      existing.value = 'true';
    } else {
      props.push({
        type: 'comment',
        value: ' bench bundle opt-in â€” read by android/build.gradle of @comapeo/core-react-native',
      });
      props.push({ type: 'property', key: 'comapeoBench', value: 'true' });
    }
    return cfg;
  });
}

const PODFILE_ENV_LINE = "ENV['COMAPEO_BENCH'] = '1'";

function withBenchPodfile(config) {
  return withPodfile(config, (cfg) => {
    let contents = cfg.modResults.contents;

    if (contents.includes(PODFILE_ENV_LINE)) {
      // Already applied by a prior prebuild run.
      return cfg;
    }

    // Prepend at the top so it runs before any autolinking-block code
    // that evaluates `ComapeoCore.podspec` (which reads the env var).
    cfg.modResults.contents =
      `# @generated by with-comapeo-bench (apps/benchmark/plugins) â€” bench-only opt-in\n` +
      `${PODFILE_ENV_LINE}\n\n` +
      contents;
    return cfg;
  });
}

const RENAME_SCRIPT_NAME = 'with-comapeo-bench: rename nodejs-project-bench â†’ nodejs-project';
const RENAME_SCRIPT_BODY = [
  '#!/bin/sh',
  '# @generated by with-comapeo-bench (apps/benchmark/plugins).',
  '# CocoaPods copies the bench bundle to <App>.app/nodejs-project-bench/.',
  '# The native loader reads from <App>.app/nodejs-project/ (a path that',
  '# does not change between flavors â€” see ios/ComapeoCore.podspec). Move',
  '# the bench bundle into place after Copy Bundle Resources so the',
  '# loader picks up bench code without changing the lookup path.',
  'set -e',
  'TARGET_DIR="${TARGET_BUILD_DIR}/${UNLOCALIZED_RESOURCES_FOLDER_PATH}"',
  'if [ -d "${TARGET_DIR}/nodejs-project-bench" ]; then',
  '  rm -rf "${TARGET_DIR}/nodejs-project"',
  '  mv "${TARGET_DIR}/nodejs-project-bench" "${TARGET_DIR}/nodejs-project"',
  'fi',
].join('\n');

function withBenchIosRenameScript(config) {
  return withXcodeProject(config, (cfg) => {
    const project = cfg.modResults;
    const targets = project.pbxNativeTargetSection();
    if (!targets) return cfg;

    let appTargetUuid;
    for (const [uuid, target] of Object.entries(targets)) {
      // Skip pbxproj comment entries (uuid keys ending with `_comment`).
      if (uuid.includes('_comment')) continue;
      // Application target's `productType` is com.apple.product-type.application.
      const productType = target.productType?.replace(/['"]/g, '');
      if (productType === 'com.apple.product-type.application') {
        appTargetUuid = uuid;
        break;
      }
    }

    if (!appTargetUuid) {
      console.warn(
        'with-comapeo-bench: no application target found in pbxproj; skipping rename script',
      );
      return cfg;
    }

    // Idempotency: if a Run Script phase with our exact name already
    // exists anywhere in the pbxproj, leave it alone. (xcode-node
    // exposes script phases as a flat dict keyed by UUID; per-target
    // filtering would require walking `target.buildPhases` references,
    // but a name match is sufficient because the name is plugin-
    // generated and unique.)
    const allScriptPhases = project.hash?.project?.objects?.PBXShellScriptBuildPhase;
    if (allScriptPhases && typeof allScriptPhases === 'object') {
      for (const [key, phase] of Object.entries(allScriptPhases)) {
        if (key.endsWith('_comment')) continue;
        if (
          phase &&
          typeof phase === 'object' &&
          phase.name &&
          String(phase.name).replace(/^"|"$/g, '') === RENAME_SCRIPT_NAME
        ) {
          return cfg;
        }
      }
    }

    project.addBuildPhase(
      [],
      'PBXShellScriptBuildPhase',
      RENAME_SCRIPT_NAME,
      appTargetUuid,
      {
        shellPath: '/bin/sh',
        shellScript: RENAME_SCRIPT_BODY,
        // Run after Copy Bundle Resources so the bench dir is on disk
        // when the rename runs.
        inputPaths: [],
        outputPaths: [],
      },
    );

    return cfg;
  });
}

module.exports = withComapeoBench;

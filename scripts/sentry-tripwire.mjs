#!/usr/bin/env node
// Sentry API tripwire (issue #69): given a build that is already sending
// to a test Sentry project, poll Sentry's API and assert that a
// `comapeo.boot` trace actually arrived with the expected structure.
// Catches the regression class unit tests can't: wiring that compiles and
// runs but never reaches Sentry, or arrives with the wrong shape.
//
// Usage:
//   SENTRY_AUTH_TOKEN=sntrys_... node scripts/sentry-tripwire.mjs \
//     --org <org-slug> --project <project-slug> \
//     (--trace <32-hex trace id> | --environment <environment>) \
//     [--platform android|ios] [--release <release>] \
//     [--timeout <seconds>] [--stats-period <period>] [--base-url <url>]
//
//   node scripts/sentry-tripwire.mjs --help
//   node scripts/sentry-tripwire.mjs --print-query --environment smoke-1  # dry run, no network
//
// Inputs:
//   SENTRY_AUTH_TOKEN   auth token with project + event read scopes
//                       (org auth token or a user token with event:read).
//   SENTRY_ORG / SENTRY_PROJECT
//                       fallbacks for --org / --project.
//   --trace             poll for a specific trace id. The boot trace id is
//                       generated by the native SDK at each launch (see
//                       docs/sentry-integration.md §7.3.2), so grab it from
//                       the trace view or device logs when using this.
//   --environment       poll for the newest comapeo.boot transaction in
//                       this Sentry environment instead. Point a test build
//                       at a distinctive value via the config plugin's
//                       `sentry.environment` option — the same value flows
//                       to the RN, native, and Node SDKs, so it uniquely
//                       marks one test build with no code changes.
//
// What it asserts (see scripts/lib/sentry-tripwire-core.mjs):
//   - a comapeo.boot transaction exists, op == "comapeo.boot"
//   - expected native child spans (Android: boot.fgs-launch,
//     boot.node-spawn, boot.rootkey-load; iOS: no fgs-launch)
//   - Node-side boot.loader-init and boot.manager-init transactions arrived
//     in the same trace (cross-process trace stitching)
//   - contexts.device.family == "Android" on the FGS transaction
//   - proc / layer / comapeo.rn tags
//   - no PII markers (rootkey / coordinates) in any fetched event
//
// Exit code 0 when everything holds within the polling window, 1 otherwise.

import { parseArgs } from "node:util";
import {
  buildDiscoveryQuery,
  evaluateBootTrace,
} from "./lib/sentry-tripwire-core.mjs";

const POLL_INTERVAL_MS = 5_000;
const MAX_TRACE_EVENTS = 20;

const { values: parsed } = parseArgs({
  options: {
    org: { type: "string" },
    project: { type: "string" },
    trace: { type: "string" },
    environment: { type: "string" },
    release: { type: "string" },
    platform: { type: "string", default: "android" },
    timeout: { type: "string", default: "60" },
    "stats-period": { type: "string", default: "1h" },
    "base-url": { type: "string", default: "https://sentry.io" },
    "print-query": { type: "boolean", default: false },
    help: { type: "boolean", default: false },
  },
});

const args = {
  ...parsed,
  org: parsed.org ?? process.env.SENTRY_ORG,
  project: parsed.project ?? process.env.SENTRY_PROJECT,
};

if (args.help) {
  const source = await import("node:fs/promises").then((fs) =>
    fs.readFile(new URL(import.meta.url), "utf8"),
  );
  // The leading comment block is the usage doc — print it verbatim.
  const usage = [];
  for (const line of source.split("\n").slice(1)) {
    if (!line.startsWith("//")) break;
    usage.push(line.replace(/^\/\/ ?/, ""));
  }
  console.log(usage.join("\n"));
  process.exit(0);
}

if (!args.trace && !args.environment) {
  fail("Pass --trace <id> or --environment <env> (see --help)");
}
if (args.trace && args.environment) {
  fail("Pass only one of --trace / --environment");
}
if (args.platform !== "android" && args.platform !== "ios") {
  fail(`--platform must be android or ios, got ${args.platform}`);
}

const query = buildDiscoveryQuery({
  traceId: args.trace,
  environment: args.environment,
});

if (args["print-query"]) {
  console.log(`Discovery query: ${query}`);
  console.log(
    `Discovery URL:   ${args["base-url"]}/api/0/organizations/${args.org ?? "<org>"}/events/` +
      `?dataset=transactions&statsPeriod=${args["stats-period"]}&query=${encodeURIComponent(query)}`,
  );
  process.exit(0);
}

if (!args.org || !args.project) {
  fail("Pass --org/--project or set SENTRY_ORG/SENTRY_PROJECT");
}
const token = process.env.SENTRY_AUTH_TOKEN;
if (!token) fail("SENTRY_AUTH_TOKEN is not set");

const timeoutMs = Number(args.timeout) * 1000;
if (!Number.isFinite(timeoutMs) || timeoutMs <= 0) {
  fail(`--timeout must be a positive number of seconds, got ${args.timeout}`);
}

/** @param {string} message @returns {never} */
function fail(message) {
  console.error(`sentry-tripwire: ${message}`);
  process.exit(1);
}

/** @param {string} path @returns {Promise<any>} */
async function api(path) {
  const response = await fetch(`${args["base-url"]}/api/0${path}`, {
    headers: { Authorization: `Bearer ${token}` },
  });
  if (!response.ok) {
    throw new Error(
      `GET /api/0${path} -> ${response.status} ${await response.text()}`,
    );
  }
  return response.json();
}

// The org-level Discover endpoint is the one that can query transactions
// (the per-project events list only returns error events); it takes the
// numeric project id, so resolve the slug first.
const projectId = (await api(`/projects/${args.org}/${args.project}/`)).id;

/**
 * One full attempt: find the boot transaction, fetch every transaction in
 * its trace, evaluate. Returns the report plus context for logging.
 */
async function attempt() {
  const fields = ["id", "trace", "transaction", "timestamp"]
    .map((f) => `field=${f}`)
    .join("&");
  const discover = await api(
    `/organizations/${args.org}/events/?dataset=transactions&${fields}` +
      `&project=${projectId}&statsPeriod=${args["stats-period"]}` +
      `&sort=-timestamp&per_page=5&query=${encodeURIComponent(query)}`,
  );
  const bootRow = discover.data?.[0];
  if (!bootRow) {
    return { report: null, message: `no comapeo.boot event for "${query}" yet` };
  }

  const traceRows = await api(
    `/organizations/${args.org}/events/?dataset=transactions&${fields}` +
      `&project=${projectId}&statsPeriod=${args["stats-period"]}` +
      `&sort=-timestamp&per_page=${MAX_TRACE_EVENTS}` +
      `&query=${encodeURIComponent(`trace:${bootRow.trace}`)}`,
  );
  const ids = (traceRows.data ?? []).map((/** @type {any} */ row) => row.id);
  const payloads = await Promise.all(
    ids.map((/** @type {string} */ id) =>
      api(`/projects/${args.org}/${args.project}/events/${id}/json/`),
    ),
  );

  const report = evaluateBootTrace(payloads, {
    platform: /** @type {"android" | "ios"} */ (args.platform),
    environment: args.environment,
    release: args.release,
  });
  return { report, traceId: bootRow.trace, eventCount: payloads.length };
}

const deadline = Date.now() + timeoutMs;
let last;
for (;;) {
  last = await attempt();
  if (last.report?.ok) break;
  const note = last.report
    ? `${last.report.failures.length} assertion(s) failing`
    : last.message;
  if (Date.now() + POLL_INTERVAL_MS > deadline) break;
  console.log(`sentry-tripwire: ${note}; retrying in ${POLL_INTERVAL_MS / 1000}s...`);
  await new Promise((resolve) => setTimeout(resolve, POLL_INTERVAL_MS));
}

if (!last.report) {
  fail(
    `timed out after ${args.timeout}s: ${last.message}. ` +
      "Is the app running with the test DSN, and is the marker " +
      "(--trace/--environment) the one the build sends?",
  );
}

console.log(
  `\nTrace ${last.traceId} — checked ${last.eventCount} transaction event(s)`,
);
for (const line of last.report.info) console.log(`  info: ${line}`);
for (const line of last.report.warnings) console.log(`  warn: ${line}`);
for (const line of last.report.failures) console.log(`  FAIL: ${line}`);

if (!last.report.ok) {
  fail(`${last.report.failures.length} assertion(s) failed`);
}
console.log("sentry-tripwire: all assertions passed");
